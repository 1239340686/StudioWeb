package com.bjwz.model;

import java.util.Date;

import javax.xml.soap.Text;

public class News {
	private int newsId;
	private String newsTitle;
	private String newsCreator;
	private Date newsCreateTime;
	private String newsPhotoUrl;
	private Text newsContent;
	public Text getNewsContent() {
		return newsContent;
	}
	public void setNewsContent(Text newsContent) {
		this.newsContent = newsContent;
	}
	public Date getNewsCreateTime() {
		return newsCreateTime;
	}
	public void setNewsCreateTime(Date newsCreateTime) {
		this.newsCreateTime = newsCreateTime;
	}
	public String getNewsPhotoUrl() {
		return newsPhotoUrl;
	}
	public void setNewsPhotoUrl(String newsPhotoUrl) {
		this.newsPhotoUrl = newsPhotoUrl;
	}
	public int getNewsId() {
		return newsId;
	}
	public void setNewsId(int newsId) {
		this.newsId = newsId;
	}
	public String getNewsTitle() {
		return newsTitle;
	}
	public void setNewsTitle(String newsTitle) {
		this.newsTitle = newsTitle;
	}
	public String getNewsCreator() {
		return newsCreator;
	}
	public void setNewsCreator(String newsCreator) {
		this.newsCreator = newsCreator;
	}
	
	
}
