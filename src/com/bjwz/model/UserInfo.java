package com.bjwz.model;

import java.util.Date;

public class UserInfo {

	private int stuId;
	private String stuName;
	private String stuSex;
	private int stuAge;
	private String stuAddress;
	private String stuPhone;
	private String stuHome;
	private Date stuBirthday;
	private int stuPassword;
	
	public int getStuId() {
		return stuId;	
	}
	public void setStuId(int stuId) {
		this.stuId = stuId;
	}
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	public String getStuSex() {
		return stuSex;
	}
	public void setStuSex(String stuSex) {
		this.stuSex = stuSex;
	}
	public int getStuAge() {
		return stuAge;
	}
	public void setStuAge(int stuAge) {
		this.stuAge = stuAge;
	}
	public String getStuAddress() {
		return stuAddress;
	}
	public void setStuAddress(String stuAddress) {
		this.stuAddress = stuAddress;
	}
	public String getStuPhone() {
		return stuPhone;
	}
	public void setStuPhone(String stuPhone) {
		this.stuPhone = stuPhone;
	}
	public String getStuHome() {
		return stuHome;
	}
	public void setStuHome(String stuHome) {
		this.stuHome = stuHome;
	}
	public Date getStuBirthday() {
		return stuBirthday;
	}
	public void setStuBirthday(Date stuBirthday) {
		this.stuBirthday = stuBirthday;
	}
	public int getStuPassword() {
		return stuPassword;
	}
	public void setStuPassword(int stuPassword) {
		this.stuPassword = stuPassword;
	}

}
